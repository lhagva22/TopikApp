LOOSE_OPTION_LINE = re.compile(
    r"(?m)^\s*(?:[①②③④]|"
    r"[\(\[]?[0-9OQDoa@<>©®][\)\]]?|"
    r"[\(\[][0-9OQDoa@<>©®][\)\]]"
    r")\s+(?=[\(가-힣A-Za-z])"
)


def loose_split_block(block: str) -> tuple[str, list[str]]:
    body = sanitize_stem_text(block)
    matches = list(LOOSE_OPTION_LINE.finditer(body))
    if len(matches) < 4:
        return body, []
    matches = matches[-4:] if len(matches) > 4 else matches
    stem = sanitize_stem_text(body[: matches[0].start()])
    options: list[str] = []
    for index, match in enumerate(matches):
        start = match.end()
        end = matches[index + 1].start() if index + 1 < len(matches) else len(body)
        text = body[start:end]
        text = re.split(r"\n(?:TOPIK|제\d+회 한국어능력시험|※\s*\[)", text, maxsplit=1)[0]
        text = text.replace("\n", " ")
        options.append(normalize_text(text))
    return stem, options


