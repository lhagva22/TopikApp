def clean_listening_page(text: str) -> str:
    lines: list[str] = []
    for raw_line in text.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if "한국어능력시험" in line or "Test of Proficiency" in line:
            continue
        if "듣기 통합" in line:
            continue
        if re.fullmatch(r"\d+", line):
            continue
        lines.append(line)
    text = "\n".join(lines)
    text = re.sub(r"(?m)^(\d{1,2})\s+(?=(?:\(|[가-힣<]))", r"\1. ", text)
    return normalize_text(text)


def clean_reading_page(text: str) -> str:
    lines: list[str] = []
    for raw_line in text.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if "한국어능력시험" in line or "Test of Proficiency" in line:
            continue
        if "TOPIK" in line and "읽기" in line:
            continue
        if re.fullmatch(r"\d+", line):
            continue
        lines.append(line)
    text = "\n".join(lines)
    text = re.sub(r"(?m)^(\d{1,2})\s+(?=(?:\(|[가-힣<]))", r"\1. ", text)
    return normalize_text(text)


def build_questions() -> list[dict[str, object]]:
    listening_pages = get_page_texts(
        LISTENING_PDF,
        range(1, pdf_page_count(LISTENING_PDF) + 1),
        OCR_CACHE_ROOT / "listening",
        clean_listening_page,
    )
    reading_pages = get_page_texts(
        READING_PDF,
        range(5, pdf_page_count(READING_PDF) + 1),
        OCR_CACHE_ROOT / "reading",
        clean_reading_page,
    )
    if 7 in reading_pages:
        match = re.search(r"11[\.,]\s*\|?\s*(?=(?:\(|[가-힣A-Z<]))", reading_pages[7])
        if match:
            reading_pages[7] = reading_pages[7][match.start() :]
    listening_blocks = build_page_mapped_blocks(listening_pages, LISTENING_PAGE_MAP, LISTENING_QUESTION_PATTERN)
    reading_blocks = build_page_mapped_blocks(reading_pages, READING_PAGE_MAP, READING_QUESTION_PATTERN)

    questions: list[dict[str, object]] = []
    for q_num in range(1, 51):
        listen_override = LISTENING_FULL_OVERRIDES.get(q_num)
        if listen_override is not None:
            stem = str(listen_override["stem"])
            options = list(listen_override["options"])  # type: ignore[arg-type]
        else:
            stem, options = loose_split_block(listening_blocks[q_num])
        if q_num in LISTENING_OPTION_IMAGE_URLS:
            options = ["①", "②", "③", "④"]
        if not options:
            raise ValueError(f"Missing listening options for question {q_num}")
        answer_index = LISTENING_ANSWERS[q_num][0] - 1
        questions.append(
            {
                "section": "listening",
                "question_number": q_num,
                "question_text": stem,
                "question_image_url": None,
                "audio_url": AUDIO_URL,
                "option_image_urls": LISTENING_OPTION_IMAGE_URLS.get(q_num),
                "options": options,
                "question_score": LISTENING_ANSWERS[q_num][1],
                "correct_answer_text": options[answer_index],
            }
        )

    for raw_q_num in range(1, 51):
        output_q_num = raw_q_num + 50
        read_override = READING_FULL_OVERRIDES.get(raw_q_num)
        if read_override is not None:
            stem = str(read_override["stem"])
            options = list(read_override["options"])  # type: ignore[arg-type]
        elif output_q_num in READING_IMAGE_QUESTION_OPTIONS:
            stem = READING_TEXT_OVERRIDES[output_q_num]
            options = READING_IMAGE_QUESTION_OPTIONS[output_q_num]
        else:
            stem, options = loose_split_block(reading_blocks[raw_q_num])
        question_text = READING_TEXT_OVERRIDES.get(output_q_num, stem)
        if not options:
            raise ValueError(f"Missing reading options for question {output_q_num}")
        answer_index = READING_ANSWERS[raw_q_num][0] - 1
        questions.append(
            {
                "section": "reading",
                "question_number": output_q_num,
                "question_text": question_text,
                "question_image_url": READING_IMAGE_URLS.get(output_q_num),
                "audio_url": None,
                "option_image_urls": None,
                "options": options,
                "question_score": READING_ANSWERS[raw_q_num][1],
                "correct_answer_text": options[answer_index],
            }
        )
    return questions


def build_assets() -> None:
    if not LISTENING_AUDIO.exists():
        raise FileNotFoundError(f"Missing audio file: {LISTENING_AUDIO}")
    copy_asset(LISTENING_AUDIO, UPLOAD_ROOT, Path("audio") / AUDIO_FILENAME)

    for question_number, meta in LISTENING_IMAGE_CROPS.items():
        for index, box in enumerate(meta["boxes"], start=1):
            output_path = ASSETS_ROOT / "debug_pages" / f"q{question_number}_option_{index}.png"
            crop_rendered_region(LISTENING_PDF, int(meta["page"]), box, output_path, scale=1.5)
            copy_asset(output_path, UPLOAD_ROOT, Path("listening") / f"q{question_number}_option_{index}.png")

    for _, meta in READING_IMAGE_CROPS.items():
        output_path = ASSETS_ROOT / "debug_pages" / str(meta["name"])
        crop_rendered_region(READING_PDF, int(meta["page"]), meta["box"], output_path, scale=1.5)
        copy_asset(output_path, UPLOAD_ROOT, Path("reading") / str(meta["name"]))


def build_sql(questions: list[dict[str, object]]) -> str:
    lines = [
        "-- Generated by scripts/generate_topik_ii_64_sql.py",
        "BEGIN;",
        "",
        "INSERT INTO mock_test_bank (",
        "  title, exam_type, test_number, description, total_questions, duration, listening_questions, reading_questions",
        ")",
        "SELECT",
        "  'TOPIK II 64-р шалгалт',",
        "  'TOPIK_II',",
        "  64,",
        "  '64-р албан ёсны TOPIK II шалгалт',",
        "  100,",
        "  130,",
        "  50,",
        "  50",
        "WHERE NOT EXISTS (",
        "  SELECT 1 FROM mock_test_bank WHERE exam_type = 'TOPIK_II' AND test_number = 64",
        ");",
        "",
        f"UPDATE mock_test_bank SET title = 'TOPIK II 64-р шалгалт', description = '64-р албан ёсны TOPIK II шалгалт', total_questions = 100, duration = 130, listening_questions = 50, reading_questions = 50, updated_at = NOW() WHERE id = {MOCK_TEST_ID_SQL};",
        "",
        f"DELETE FROM mock_test_questions WHERE mock_test_id = {MOCK_TEST_ID_SQL};",
        "",
        "INSERT INTO mock_test_questions (",
        "  mock_test_id,",
        "  section,",
        "  question_number,",
        "  question_text,",
        "  question_image_url,",
        "  audio_url,",
        "  option_image_urls,",
        "  options,",
        "  question_score,",
        "  correct_answer_text,",
        "  explanation",
        ") VALUES",
    ]
    values: list[str] = []
    for question in questions:
        values.append(
            "  (" + ", ".join(
                [
                    MOCK_TEST_ID_SQL,
                    sql_string(str(question["section"])),
                    str(question["question_number"]),
                    sql_string(str(question["question_text"])),
                    sql_string(question["question_image_url"] if isinstance(question["question_image_url"], str) else None),
                    sql_string(question["audio_url"] if isinstance(question["audio_url"], str) else None),
                    sql_json_array(question["option_image_urls"]) if isinstance(question["option_image_urls"], list) else "NULL",
                    sql_json_array(question["options"]),  # type: ignore[arg-type]
                    str(question["question_score"]),
                    sql_string(str(question["correct_answer_text"])),
                    "NULL",
                ]
            ) + ")"
        )
    lines.append(",\n".join(values) + ";")
    lines.extend(["", "COMMIT;", ""])
    return "\n".join(lines)


def main() -> None:
    questions = build_questions()
    if len(questions) != 100:
        raise ValueError(f"Expected 100 questions, got {len(questions)}")
    build_assets()
    OUTPUT_SQL.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_SQL.write_text(build_sql(questions), encoding="utf-8")
    print(f"Wrote {OUTPUT_SQL}")


if __name__ == "__main__":
    main()
